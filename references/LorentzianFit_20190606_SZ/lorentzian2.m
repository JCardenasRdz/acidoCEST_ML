function lval = lorentzian2(A,w,x0,x)
% =========================================================================
% Lorentzian lineshape
% Check for more info: Cai, K., et al. (2015). NMR Biomed 28(1): 1-8.
% =========================================================================

lval = A*(w.^2)./(w.^2+4*(x-x0).^2);

end
